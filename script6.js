
let n1 = Number(prompt("Número 1:"));
let n2 = Number(prompt("Número 2:"));
let op = prompt("Operação (+, -, *, /):");

if (op === "+") {
  alert(n1 + n2);
} else if (op === "-") {
  alert(n1 - n2);
} else if (op === "*") {
  alert(n1 * n2);
} else if (op === "/") {
  alert(n1 / n2);
}

